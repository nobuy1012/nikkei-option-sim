import streamlit as st
import pandas as pd
import numpy as np
import plotly.graph_objects as go

# -----------------------------------------------------------------------------
# 1. 計算ロジックの定義 (仕様書に基づくコア部分)
# -----------------------------------------------------------------------------
def calculate_option_pnl(entry_price, current_price, strike_price, premium, quantity, option_type, side, multiplier=1000):
    """
    オプション1枚あたりの損益を計算する関数

    Parameters:
    - current_price: 評価時の原資産価格（日経平均）
    - strike_price: 権利行使価格
    - premium: 取得単価（プレミアム）
    - quantity: 枚数
    - option_type: "Call" or "Put"
    - side: "Buy" (買い) or "Sell" (売り)
    - multiplier: 取引単位（ラージなら1000、ミニなら100。デフォルト1000）

    Returns:
    - 損益額 (円)
    """

    # 本質的価値（Intrinsic Value）の計算
    if option_type == "Call":
        # コール: MAX(原資産価格 - 権利行使価格, 0)
        intrinsic_value = max(current_price - strike_price, 0)
    else: # Put
        # プット: MAX(権利行使価格 - 原資産価格, 0)
        intrinsic_value = max(strike_price - current_price, 0)

    # 損益計算（仕様書の4パターンを再現）
    # 注: 実際の金額にするため multiplier を掛けています

    if side == "Buy":
        # 買い: (本質的価値 - プレミアム) * 枚数 * 単位
        pnl = (intrinsic_value - premium) * quantity * multiplier
    else: # Sell
        # 売り: (プレミアム - 本質的価値) * 枚数 * 単位
        pnl = (premium - intrinsic_value) * quantity * multiplier

    return pnl

